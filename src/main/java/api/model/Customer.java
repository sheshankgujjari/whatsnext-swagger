package api.model;

public class Customer {

    public int customerId;

    public String firstName;

    public String middleName;

    public String lastName;

    public String address;

    public String fbAppId;

    public String fbAppSecretToken;

    public String fbAccessToken;

    public String googleClientSecret;

    public String twitterConsumerKey;

    public String twitterConsumerSecret;

    public String twitterAccessToken;

    public String twitterAccessTokenSecret;

    public String userName;

    public String password;

    public String appUniqueId;

    public int getCustomerId() { return customerId; }

    public void setCustomerId(int customerId) { this.customerId = customerId; }

    public String getFirstName() { return firstName; }

    public void setFirstName(String firstName) { this.firstName = firstName; }

    public String getMiddleName() { return middleName; }

    public void setMiddleName(String middleName) { this.middleName = middleName; }

    public String getLastName() { return lastName; }

    public void setLastName(String lastName) { this.lastName = lastName; }

    public String getAddress() { return address; }

    public void setAddress(String address) { this.address = address; }

    public String getFbAppId() { return fbAppId; }

    public void setFbAppId(String fbAppId) { this.fbAppId = fbAppId; }

    public String getFbAppSecretToken() { return fbAppSecretToken; }

    public void setFbAppSecretToken(String fbAppSecretToken) { this.fbAppSecretToken = fbAppSecretToken; }

    public String getFbAccessToken() { return fbAccessToken; }

    public void setFbAccessToken(String fbAccessToken) { this.fbAccessToken = fbAccessToken; }

    public String getGoogleClientSecret() { return googleClientSecret; }

    public void setGoogleClientSecret(String googleClientSecret) { this.googleClientSecret = googleClientSecret; }

    public String getTwitterConsumerKey() { return twitterConsumerKey; }

    public void setTwitterConsumerKey(String twitterConsumerKey) { this.twitterConsumerKey = twitterConsumerKey; }

    public String getTwitterConsumerSecret() { return twitterConsumerSecret; }

    public void setTwitterConsumerSecret(String twitterConsumerSecret) { this.twitterConsumerSecret = twitterConsumerSecret; }

    public String getTwitterAccessToken() { return twitterAccessToken; }

    public void setTwitterAccessToken(String twitterAccessToken) { this.twitterAccessToken = twitterAccessToken; }

    public String getTwitterAccessTokenSecret() { return twitterAccessTokenSecret; }

    public void setTwitterAccessTokenSecret(String twitterAccessTokenSecret) { this.twitterAccessTokenSecret = twitterAccessTokenSecret; }

    public String getUserName() { return userName; }

    public void setUserName(String userName) { this.userName = userName; }

    public String getPassword() { return password; }

    public void setPassword(String password) { this.password = password; }

    public String getAppUniqueId() { return appUniqueId; }

    public void setAppUniqueId(String appUniqueId) { this.appUniqueId = appUniqueId; }
}
